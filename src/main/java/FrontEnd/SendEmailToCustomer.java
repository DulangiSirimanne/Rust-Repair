    private void sendCustomerNotificationEmail() {

        String jobID = job_idTextField.getText();
        String jobFee = job_feeTextField.getText();
        String vehicleNo = veh_NoTextField.getText();
        String cusID = cus_idTextField.getText();
        String cusName = cus_nameTextField.getText();
        String cusEmail = cus_emailTextField.getText();

        if (!jobID.isEmpty() && !jobFee.isEmpty() && !vehicleNo.isEmpty() && !cusID.isEmpty() && !cusName.isEmpty() && !cusEmail.isEmpty()) {

            SendEmail sm = new SendEmail();
            sm.SendEmailToCustomer(jobID, jobFee, vehicleNo, cusID, cusName, cusEmail);
        } else if (!jobID.isEmpty() || !jobFee.isEmpty() || !vehicleNo.isEmpty() || !cusID.isEmpty() || !cusName.isEmpty() || !cusEmail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields before save.", "Empty fields", JOptionPane.OK_OPTION);
            cus_idTextField.requestFocus();
        }
    }

