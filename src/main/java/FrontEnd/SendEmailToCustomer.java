    private void loadDetailsToTable() {
        try {
            int rowCount = 0;
            Statement stmt = ConnectionMSSQL.connect().createStatement();
            String query = "SELECT CustomerTable.CustomerID AS CustomerTableCustomerID,\n"
                    + "CustomerTable.CustomerName AS CustomerTableCustomerName,\n"
                    + "CustomerTable.CustomerEmail AS CustomerTableCustomerEmail,\n"
                    + "CustomerTable.VehicleID AS CustomerTableVehicleID,\n"
                    + "JobTable.JobID AS JobTableJobID,\n"
                    + "JobTable.JobFee AS JobTableJobFee \n"
                    + "FROM CustomerTable INNER JOIN JobTable ON CustomerTable.CustomerID = JobTable.CustomerID\n"
                    + "WHERE JobTable.JobStatus = 'Complete'";
            ResultSet rs = stmt.executeQuery(query);

            while (rs.next()) {
                model_tableCustomerEmail.addRow(new Object[model_tableCustomerEmail.getColumnCount()]);
                tableCustomerEmail.setValueAt(rs.getString("CustomerTableCustomerID"), rowCount, 0);
                tableCustomerEmail.setValueAt(rs.getString("CustomerTableCustomerName"), rowCount, 1);
                tableCustomerEmail.setValueAt(rs.getString("CustomerTableCustomerEmail"), rowCount, 2);
                tableCustomerEmail.setValueAt(rs.getString("CustomerTableVehicleID"), rowCount, 3);
                tableCustomerEmail.setValueAt(rs.getString("JobTableJobID"), rowCount, 4);
                tableCustomerEmail.setValueAt(rs.getString("JobTableJobFee"), rowCount, 5);
                rowCount++;
            }
            rs.close();

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SendEmailToCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SendEmailToCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

